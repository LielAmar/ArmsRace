package com.lielamar.armsrace.modules.shop;

public class TrailData {

    private final int red, green, blue;

    public TrailData(int r, int g, int b) {
        this.red = r;
        this.green = g;
        this.blue = b;
    }

    public int getRed() {
        return red;
    }

    public int getGreen() {
        return green;
    }

    public int getBlue() {
        return blue;
    }

}
